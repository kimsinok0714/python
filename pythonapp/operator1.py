# 산술 연산에 대한 예제 프로그램입니다.

# a = 7
# b = 4

a, b = 7, 4

print(f'{a} + {b} = {a + b}')
print(f'{a} - {b} = {a - b}')
print(f'{a} * {b} = {a * b}')
print(f'{a} // {b} = {a // b}')  # 몫
print(f'{a} / {b} = {a / b}')  
print(f'{a} % {b} = {a % b}')    # 나머지


num1 = num2 = num3 = 100

print(f'num1 = {num1}, num2 = {num2}, num3 = {num3}')

del num1

